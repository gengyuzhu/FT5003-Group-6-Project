// ---- Helper functions ----
export function getNFTById(id) {
  return ALL_NFTS.find((nft) => nft.id === Number(id)) || null;
}

export function getCollectionBySlug(slug) {
  return COLLECTIONS.find((c) => c.slug === slug) || null;
}

export function getCollectionById(id) {
  return COLLECTIONS.find((c) => c.id === Number(id)) || null;
}

export function getNFTsByCollection(collectionId) {
  return ALL_NFTS.filter((nft) => nft.collectionId === Number(collectionId));
}

export function getFeaturedNFTs() {
  return ALL_NFTS.slice(0, 8);
}

export function getTrendingCollections() {
  return [...COLLECTIONS].sort(
    (a, b) => parseFloat(b.totalVolume) - parseFloat(a.totalVolume)
  );
}

